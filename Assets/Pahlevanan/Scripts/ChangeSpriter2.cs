using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class ChangeSpriter2 : MonoBehaviour
{


    public Sprite[] defaultOutfitSprites; // ??????? ???????? ??? ???
    private string currentOutfit = "defaultOutfit"; // ??? ???? ????
    private int outfitIndex = 0; // ????? ???? ????

    void Start()
    {
        SetOutfit(currentOutfit);
    }

    void SetOutfit(string outfit)
    {
        currentOutfit = outfit;
        SpriteRenderer[] renderers = GetComponentsInChildren<SpriteRenderer>();
        for (int i = 0; i < renderers.Length; i++)
        {
            SpriteRenderer renderer = renderers[i];
            if (renderer.sprite == defaultOutfitSprites[0] || renderer.sprite == defaultOutfitSprites[1] || renderer.sprite == defaultOutfitSprites[2])
            {
                // ??? ??????? ???? ??? ????
                if (outfit == "defaultOutfit")
                {
                    // ??? ???? ??????? ????
                    renderer.sprite = defaultOutfitSprites[i];
                }
                else
                {
                    // ??? ???? ???? ????
                    renderer.sprite = Resources.Load<Sprite>(outfit + "_body_" + i);
                }
            }
            else
            {
                // ??? ??????? ???? ????? ????
                renderer.sprite = Resources.Load<Sprite>(outfit + "_clothes_" + i);
            }
        }
    }

    void Update()
    {
        // ????? ???????? ??? ??? ?? ????? ???????
        int defaultOutfitCount = defaultOutfitSprites.Length / 5;

        // ????? ?? ??????? ?? ????? ??? ???
        for (int i = 1; i <= defaultOutfitCount; i++)
        {
            if (Input.GetKeyDown(KeyCode.Alpha0 + i))
            {
                // ????? ???? ????
                outfitIndex = i - 1;
                SetOutfit("outfit" + outfitIndex);
            }
        }

        // ????? ?? ???? ????? ???
        if (Input.GetKeyDown(KeyCode.Space))
        {
            // ?????? ?? ???? ???????
            SetOutfit("defaultOutfit");
        }
    }
}


